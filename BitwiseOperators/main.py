# & - conjunction operator - returns 1 only if both the binaries are 1
print(15 & 22)
# | - disjunction operator - returns 1 if at least one binary is 1
print(15 | 22)
# ^ - exclusive OR operator - returns 1 only if exactly one binary is 1
print(15 ^ 22)
# ~ - negation operator - returns opposite of the bit
print(~22)
# >> - bit right shift operator - shifts all the bits in binary to the right
print(22 >> 1)
# << - bit left shift operator - shifts all the bits in binary to the left
print(22 << 2)
